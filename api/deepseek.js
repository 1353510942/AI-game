export default async function handler(req, res) {

    const API_KEY = process.env.DEEPSEEK_KEY;

    if (!API_KEY) {
        return res.status(500).json({ error: "缺少API Key，请检查Vercel环境变量" });
    }

    const { type, difficulty, character, question, hintLevel, stats } = req.body;

    let prompt = "";

    // =========================
    // 🎯 出题系统（无题库限制）
    // =========================
    if (type === "start") {

        const seed = Date.now() % 10000;
        const rand = Math.floor(Math.random() * 1000);

        let difficultyCondition = "";

        if (difficulty === "easy") {
            difficultyCondition = `
难度：简单
条件：
- 该人物必须出现在中国中小学（小学、初中、高中）历史、语文、数学、物理、政治等教科书中
- 或是在全球范围内家喻户晓、中学生普遍知晓的世界历史名人
- 覆盖范围：中国古代、中国近现代、世界历史各个时期均可
- 人物类型可以是：政治家、思想家、科学家、文学家、军事家、艺术家等
- 要有足够的多样性，不要总选同一类型或同一时代的人物
`;
        } else if (difficulty === "hard") {
            difficultyCondition = `
难度：困难
条件：
- 该人物不应出现在中小学教科书中，但在大学历史课程或历史爱好者圈子中有一定知名度
- 例如类型：中亚征服者、中世纪欧洲君主、非洲帝国统治者、东南亚历史人物、奥斯曼/波斯/莫卧儿帝国人物、拜占庭皇帝、中国非主流朝代人物等
- 覆盖范围：全球各大洲、各文明圈，不局限于某一地区
- 要有高度地域多样性，不要总选同一地区或文明的人物
`;
        } else {
            difficultyCondition = `
难度：地狱
条件：
- 该人物极度冷门，即使是历史爱好者也未必知晓，只有相关领域研究学者才熟悉
- 例如类型：古埃及中期法老、古巴比伦/亚述国王、古印度小王朝君主、古波斯地方总督或天文学家、古希腊小城邦人物、古罗马非主流人物、中世纪中亚或非洲冷门人物、中国冷门朝代地方官员或学者等
- 覆盖范围：全球，跨越各文明、各时代，强调极度冷门和多样性
- 要有极高的随机性和多样性，绝对不要总选同一地区的人物
`;
        }

        prompt = `
你是历史游戏出题系统。

${difficultyCondition}

随机种子（用于保证每次选择不同人物）：${seed}-${rand}

⚠️严格规则：
- 必须选择真实存在的历史人物
- 不允许犯常识性错误（例如：孔子名丘，字仲尼，不能说"孔子姓孔"这种笼统表述）
- 人物必须有可验证的历史记载
- 每次调用都应选择不同的人物，利用随机种子保证多样性
- 只输出JSON格式，不要输出任何其他内容

输出格式（严格遵守，只输出这个JSON，不要有任何前缀或后缀）：
{
  "name": "人物中文名（如有通用中文译名则使用译名）",
  "bio": "一句话简介（中文，不超过30字）"
}
`;
    }

    // =========================
    // ❓ 提问判断系统
    // =========================
    if (type === "ask") {
        prompt = `
你是历史问答裁判。

当前人物：${character}

玩家问题：${question}

严格规则：
- 只能回答"是"、"否"、"不确定"三种答案之一，不能有任何额外说明
- 绝对禁止透露人物的姓名、字号、封号或任何能直接识别身份的称谓
- 不能给额外提示或解释
- 必须用中文回答
- 只输出一个词："是"、"否"或"不确定"
`;
    }

    // =========================
    // 💡 递进提示系统
    // =========================
    if (type === "hint") {

        const level = parseInt(hintLevel) || 0;

        let hintInstruction = "";

        if (level === 0) {
            hintInstruction = `
给出【第一级提示】（最模糊）：
- 只说明该人物所处的大致历史时期（如：上古时期、古典时代、中世纪、近代早期、近现代）
- 只说明该人物所在的大致地理区域（如：东亚、南亚、中亚、西亚、北非、欧洲、美洲）
- 绝对不能说具体国家名称
- 绝对不能透露职业、身份、成就或任何事件
- 绝对不能说出或暗示人物姓名
示例风格："这位人物活跃于中世纪的西亚地区"
`;
        } else if (level === 1) {
            hintInstruction = `
给出【第二级提示】：
- 可以说明职业大类（如：统治者、科学家、思想家、军事家、艺术家、宗教人物）
- 可以说明该人物活跃的大致领域
- 仍然不能说具体国家名称，不能说具体事件名称
- 绝对不能说出或暗示人物姓名
示例风格："这位人物是一位古代东亚的思想家，在哲学和教育领域有深远影响"
`;
        } else if (level === 2) {
            hintInstruction = `
给出【第三级提示】：
- 可以说明具体国家、王朝或文明
- 可以说明一项代表性成就（但不能太具体到能直接搜索出人名）
- 绝对不能直接说出或暗示人物姓名
示例风格："这位人物是中国古代一个大一统王朝的建立者，结束了长期的分裂局面"
`;
        } else if (level === 3) {
            hintInstruction = `
给出【第四级提示】：
- 可以说明该人物的具体身份定位（例如：某王朝第几位皇帝、某领域的奠基人）
- 可以说明两项以上代表性成就或历史事件
- 仍然绝对不能直接说出人物姓名
示例风格："这位人物是中国秦朝的开国皇帝，统一了文字和度量衡，修建了大型防御工程"
`;
        } else {
            hintInstruction = `
给出【最终提示】（最详细，但仍不说名字）：
- 可以说明非常具体的身份信息
- 可以说明多项标志性成就、相关事件
- 可以给出足以让玩家锁定人物的关键信息
- 但仍然绝对不能直接说出人物姓名或字号
示例风格："这位人物统治了中国历史上第一个中央集权帝国，推行郡县制，统一了六国，修建了连接各地的驰道"
`;
        }

        prompt = `
你是游戏提示系统。

当前人物：${character}

${hintInstruction}

⚠️绝对禁止：
- 禁止出现人物的任何姓名、字号、封号、庙号、谥号或明显的识别性称谓
- 禁止文言文表达
- 禁止诗意化或比喻性语言
- 禁止英文
- 必须使用现代中文白话文
- 只输出一句提示，不要有任何前缀标签或解释
`;
    }

    // =========================
    // 🏁 结算系统
    // 注意：链接由前端生成，这里只输出人物介绍和得分数字
    // =========================
    if (type === "end") {

        const { ask = 0, hint = 0, guess = 0 } = stats || {};

        let baseScore = 100;
        if (difficulty === "hard") baseScore = 150;
        if (difficulty === "hell") baseScore = 200;

        const deduction = (ask * 5) + (hint * 10) + (guess * 8);
        const finalScore = Math.max(0, baseScore - deduction);

        prompt = `
你是游戏结算系统。

本局人物：${character}
本局得分：${finalScore}

请严格按照以下格式输出，全部使用现代中文白话文，禁止任何英文，禁止文言文：

【人物生平】
用200字左右介绍该人物的生平，包括出生背景、主要经历、重要事件，语言通俗易懂。如史料较少请注明"史料记载有限"。

【主要成就】
• 成就一
• 成就二
• 成就三（如有）

【历史影响】
用一到两句话说明该人物对历史的影响和意义。

【本局得分】
${finalScore}分

⚠️规则：
- 全部使用中文，绝对禁止英文单词
- 禁止文言文
- 语言通俗易懂
- 严格按照上述四个区块格式输出，不要增减区块，不要改变区块标题
`;
    }

    // =========================
    // 🤖 调用 DeepSeek
    // =========================
    try {
        const response = await fetch("https://api.deepseek.com/v1/chat/completions", {
            method: "POST",
            headers: {
                "Content-Type": "application/json",
                "Authorization": `Bearer ${API_KEY}`
            },
            body: JSON.stringify({
                model: "deepseek-chat",
                messages: [
                    {
                        role: "system",
                        content: "你是严格的历史游戏AI系统。规则：①只输出要求格式的内容；②全程禁止英文；③禁止文言文；④使用现代中文白话文；⑤禁止在提示中透露人物姓名；⑥每次出题必须保证人物多样性。"
                    },
                    {
                        role: "user",
                        content: prompt
                    }
                ],
                temperature: 0.9
            })
        });

        const data = await response.json();

        return res.status(200).json({
            result: data.choices?.[0]?.message?.content || "AI未返回内容"
        });

    } catch (err) {
        return res.status(500).json({
            result: "调用失败：" + err.message
        });
    }
}